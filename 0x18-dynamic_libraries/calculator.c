#include "calc.h"

/**
 * add - perform addition
 *
 * Return: an integer
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - perform subtraction
 *
 * Return: an integer
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - perform division
 *
 * Return: an integer
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - perform division
 *
 * Return: an integer
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - perform modulo
 *
 * Return: an integer
 */
int mod(int a, int b)
{
	return (a % b);
}
